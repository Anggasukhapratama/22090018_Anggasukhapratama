    private void BtnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnsimpanActionPerformed
        // TODO add your handling code here:
       try {
    st = cn.createStatement();
    // Check if any of the text fields are empty
    if (txtid.getText().equals("") || 
        txtid_obat.getText().equals("") || 
        txtid_pemilik.getText().equals("") || 
        txtid_species.getText().equals("") || 
        txtnama.getText().equals("") || 
        txthabitat.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "Data tidak boleh ada yang kosong", "Validasi data ", JOptionPane.INFORMATION_MESSAGE);
        return; // Return if any field is empty
    }

    if (Btnsimpan.getText().equals("Simpan")) { // Use .equals() to compare strings
        String cek = "SELECT * FROM hewan WHERE id = '" + txtid.getText() + "'";
        rs = st.executeQuery(cek);
        if (rs.next()) {
            JOptionPane.showMessageDialog(null, "Maaf, data ini sudah ada.");
        } else {
            // Corrected SQL statement to insert data
            String sql = "INSERT INTO hewan VALUES ('" + txtid.getText() + "','" + txtid_obat.getText() + "','" + txtid_pemilik.getText() + "','" + txtid_species.getText() + "','" + txtnama.getText() + "','" + txthabitat.getText() + "')";
            st.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
            Bersih();
            TampilData();
            
        }
    } else {
        
        // Handle other actions if needed
        String update = "UPDATE hewan SET id_obat = '" + txtid_obat.getText()+
               
                "',id_pemilik ='" + txtid_pemilik.getText()+
                 "',id_species ='" + txtid_species.getText()+
                 "',nama ='" + txtnama.getText()+
                 "',habitat ='" + txthabitat.getText()+
                 "' WHERE id = '" + txtid.getText()+"'";
        
        st.executeUpdate(update);
        JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
        Bersih();
        TampilData();
        
                
    }
} catch (Exception e) {
    e.printStackTrace(); // Print the exception details for debugging
    JOptionPane.showMessageDialog(null, e);
}

            
    }//GEN-LAST:event_BtnsimpanActionPerformed

