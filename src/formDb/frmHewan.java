    private void BtnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnhapusActionPerformed
        // TODO add your handling code here:
        if (txtid.getText().equals("")) {
        JOptionPane.showMessageDialog(this,"Silahkan pilih Data yang akan dihapus");
    } else {
        int jawab = JOptionPane.showConfirmDialog(null, "Data ini akan dihapus, apakah anda ingin melanjutkan?", "KONFIRMASI", JOptionPane.YES_NO_OPTION);
        if (jawab == JOptionPane.YES_OPTION) {
            // Code to delete the data goes here
            try {
                
                st = cn.createStatement();
                String  sql = "DELETE FROM hewan WHERE id = '"+ txtid.getText()+"'";
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus ");
                TampilData();
                Bersih();
           
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
 
         
            }
        }
    
    }//GEN-LAST:event_BtnhapusActionPerformed

