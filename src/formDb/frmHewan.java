    private void CariData(){
        try {
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM hewan WHERE"+ Cmbcari.getSelectedItem().toString()+"LIKE'%" + TxtCari.getText()+"%'");
            
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Nomor");
            model.addColumn("id");
            model.addColumn("id_obat");
            model.addColumn("id_pemilik");
            model.addColumn("id_species");
            model.addColumn("nama");
            model.addColumn("habitat");
            
            int no =1;
            
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            
            
            while (rs.next()){
                Object[] data ={
                    no ++,
                    rs.getString ("id"),
                     rs.getString ("id_obat"),
                      rs.getString ("id_pemilik"),
                       rs.getString ("id_species"),
                        rs.getString ("nama"),
                         rs.getString ("habitat")
                };
                model.addRow(data);
                Tbldata.setModel(model);
            }
        } catch (Exception e) {
        }
    }
